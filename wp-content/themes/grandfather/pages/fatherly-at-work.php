                <h2>Our Goals</h2>
                <div class="faw-goals-flex">
                    <div class="faw-goals-flex-content">
                        <h3>Attracting and Retaining Talent</h3>
                        <p>
                            Benefits programs are transforming how companies recruit top-level talent as a competitive job market
                            and specialization put high-salary Millennial job-seekers in the position demand healthier corporate
                            cultures. Some 93 percent of white-collar Millennial workers see parental benefits – and paternity
                            benefit uptake in particular, which is strongly indicative of gender parity in a workforce – as critical.
                            This constitutes a strong incentive for companies to offer benefits that incur significantly lower costs
                            than attrition and aid in recruiting. Fatherly at Work represents a commitment to the sort of practical
                            and humane workplace policies that allow companies to compete.
                        </p>
                        <h3>Opening Lines of Communication With Employees</h3>
                        <p>
                            From a human resources perspective, the conversation around parental benefits often orbits parent
                            leave, but the most critical goal for employers should be opening lines of communication. Leave
                            alone is not enough. This is amply demonstrated by the fact some 57 percent of men don’t take
                            advantage of parental leave offerings. Why? Because job security is the paramount concern for
                            new fathers, many of whom believe they will be penalized for taking advantage of benefits. From
                            an employer perspective, the issue here is not just distrust, but the cost of that distrust.
                            Roughly 70 percent of fathers engage in flextime, but only 10 percent of fathers formalize those
                            arrangements. That means a disproportionate number of dads practice the art of “passing” — a
                            term that describes cheating on one’s employer with one’s family. The result is sub-par performance
                            and employee resentment. The problem can be solved.
                        </p>
                        <h3>Creating Gender Parity in the Workplace</h3>
                        <p>
                            Historically, dads have been the beneficiaries of six percent earnings disparity referred to
                            by researchers as the “Fatherhood Bonus.” Moms, on the other hand, are subject to a four percent
                            “Motherhood Penalty.” The cultural reasons for this discrepancy are clear: Fathers are assumed
                            to be committed employees while mothers are assumed to be eyeing the door. This is not merely
                            sexist, it’s untrue. New fathers’ ability to participate as caregivers is positively correlated
                            with the long-term economic health of their families and support in the workplace demonstrably
                            keeps new mothers in the workforce. Therefore, it makes sense for businesses to invest in paternity
                            benefits aimed not only at the working fathers they employ but at the spouses of their working
                            mothers. Demonstrating a holistic commitment to helping working mothers improves a business’s
                            ability to retain and recruit senior-level female executives.
                        </p>
